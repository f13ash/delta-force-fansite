function generateDynamicStyles() {
    // Удаляем старые динамические стилы
    const oldStyle = document.getElementById('dynamic-styles');
    if (oldStyle) oldStyle.remove();
    
    const style = document.createElement('style');
    style.id = 'dynamic-styles';
    
    // Находим все уникальные column types
    const allColumnTypes = new Set();
    Object.values(questsDatabase).forEach(questSet => {
        Object.values(questSet).forEach(quest => {
            if (quest.column) allColumnTypes.add(quest.column);
        });
    });
    
    let css = '';
    const colors = {
        'top': '#55ff55', 
        'middle': '#ff5555', 
        'bottom': '#5566ff',
        'default': '#888888'
    };
    
    // Генерируем CSS для каждого типа колонки
    allColumnTypes.forEach(columnType => {
        const baseType = columnType.replace(/\d+$/, '');
        const color = colors[baseType] || colors.default;
        
        css += `
            .branch-${columnType} { 
                order: ${columnType.includes('top') ? 1 : columnType.includes('middle') ? 2 : 3};
                align-items: ${columnType.includes('top') ? 'flex-start' : columnType.includes('bottom') ? 'flex-end' : 'center'};
            }
            
            .branch-${columnType} .quest-node { 
                border: 2px solid ${color};
                background: ${color}22;
            }
        `;
    });
    
    style.textContent = css;
    document.head.appendChild(style);
}

