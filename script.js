function createLevelColumn(level) {
    const levelColumn = document.createElement('div');
    levelColumn.className = 'level-column';
    levelColumn.dataset.level = level;
    
    const displayType = getDisplayType();
    const columnTypes = new Set();
    
    Object.values(currentQuests).forEach(quest => {
        if (quest.level === level && quest.column) {
            columnTypes.add(quest.column);
        }
    });
    
    if (displayType === 'seasonal') {
        // Для сезонных: создаем только нужные строки
        const seasonalRows = ['top2', 'top1', 'middle', 'bottom1', 'bottom2'];
        seasonalRows.forEach(columnType => {
            if (columnTypes.has(columnType)) {
                const branchRow = document.createElement('div');
                branchRow.className = 'branch-row';
                branchRow.dataset.column = columnType;
                levelColumn.appendChild(branchRow);
            }
        });
    } else {
        // Для репутационных: создаем все строки в правильном порядке
        const allPossibleRows = [
            'top5', 'top4', 'top3', 'top2', 'top1', 
            'middle', 
            'bottom1', 'bottom2', 'bottom3', 'bottom4', 'bottom5', 'bottom6'
        ];
        
        allPossibleRows.forEach(columnType => {
            if (columnTypes.has(columnType)) {
                const branchRow = document.createElement('div');
                branchRow.className = 'branch-row';
                branchRow.dataset.column = columnType;
                levelColumn.appendChild(branchRow);
            }
        });
    }
    
    return levelColumn;
}

function updateTreeHeight() {
    const displayType = getDisplayType();
    
    if (displayType === 'seasonal') {
        // Для сезонных: фиксированная высота
        document.querySelector('.quest-tree').style.minHeight = '600px';
        document.querySelectorAll('.level-column').forEach(col => {
            col.style.height = '600px';
        });
    } else {
        // Для репутационных: динамическая высота
        let maxColumns = 0;
        const columnsPerLevel = {};
        
        Object.values(currentQuests).forEach(quest => {
            if (!columnsPerLevel[quest.level]) {
                columnsPerLevel[quest.level] = new Set();
            }
            columnsPerLevel[quest.level].add(quest.column);
        });
        
        Object.values(columnsPerLevel).forEach(columns => {
            if (columns.size > maxColumns) {
                maxColumns = columns.size;
            }
        });
        
        const treeHeight = Math.max(800, maxColumns * 120 + 200);
        document.querySelector('.quest-tree').style.minHeight = `${treeHeight}px`;
        document.querySelectorAll('.level-column').forEach(col => {
            col.style.height = `${treeHeight}px`;
        });
    }
}

